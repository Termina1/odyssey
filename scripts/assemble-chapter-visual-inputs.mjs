import { readFile, mkdir, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
const files = JSON.parse(process.env.VISUAL_FILES ?? "[]");
const inputs = [];
for (const path of files) inputs.push(JSON.parse(await readFile(resolve(process.cwd(), path), "utf8")));
const output = { sectionId: process.env.SECTION_ID, inputs };
const outputPath = resolve(process.cwd(), process.env.OUTPUT_PATH);
await mkdir(dirname(outputPath), { recursive: true });
await writeFile(outputPath, `${JSON.stringify(output, null, 2)}\n`);
console.log(JSON.stringify({ type: "VISUAL_INPUTS_READY", output: { reason: "", instructions: [] } }));
